int	w(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9') || c == '_' || c == '?');
}

int	s(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\0');
}
